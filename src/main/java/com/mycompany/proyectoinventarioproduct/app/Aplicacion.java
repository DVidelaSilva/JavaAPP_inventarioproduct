//---------------------FUNCION BOTON GUARDAR----------------------------------//
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
               
        Lista elemento = new Lista();                                   // Crear una variable "a" que trae datos del archivo Lista
        elemento.setCodigo(this.inputCodigo.getText().toUpperCase());               // esta variable a guarda los que se ingrese en el input de codigo
        elemento.setProducto(this.inputProducto.getText().toUpperCase());             // esta variable a guarda los que se ingrese en el input de producto
        elemento.setMarca(this.inputMarca.getText().toUpperCase());       // esta variable a guarda los que se ingrese en el input de marca
        elemento.setDescripcion(this.inputDescripcion.getText().toUpperCase());       // esta variable a guarda los que se ingrese en el input de descripcion
        elemento.setUbicacion(this.inputUbicacion.getText().toUpperCase());       // esta variable a guarda los que se ingrese en el input de ubicacion
        elemento.setCantidad(this.inputCantidad.getText().toUpperCase());       // esta variable a guarda los que se ingrese en el input de Cantidad
        
        if(!StringUtils.isEmptyOrWhitespaceOnly(labelId.getText())) {
            elemento.setId(labelId.getText());
        }
        
        inventarioDao dao = new inventarioDao();
        dao.guardar(elemento);
        
        actualizarOutput();                          // actualiza la lista para mostrar elementos nuevos agregados
        
        JOptionPane.showMessageDialog(rootPane, "El Item se Guardo Correctamente");  //mensaje de alerta al guardar
        limpiarInputs();                             /// llamada a la funcion que limpia los inputs
        

        
    }//GEN-LAST:event_btnGuardarActionPerformed

    
//---------------------FUNCION BOTON ELIMINAR----------------------------------//
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int indice = this.outputList.getSelectedIndex();     // permite obtener el numero del indice del elemento en el output y guardarlo como una variable indice
        Lista elemento = lista.get(indice);
        inventarioDao dao = new inventarioDao();
        dao.eliminar(elemento.getId());
        actualizarOutput();                                      // funcion de actualizar la lista para ver los cambios
        
        JOptionPane.showMessageDialog(rootPane, "Se Elimino Correctamente");  //mensaje de alerta al Eliminar
    }//GEN-LAST:event_btnEliminarActionPerformed

    
