//---------------------FUNCION LIMPIAR INPUTS----------------------------------//

    private void limpiarInputs() {
        this.inputCodigo.setText("");
        this.inputProducto.setText("");
        this.inputMarca.setText("");
        this.inputDescripcion.setText("");
        this.inputUbicacion.setText("");
        this.inputCantidad.setText("");
        this.labelId.setText("");
        
    }
    

//---------------------FUNCION ACTUALIZAR LISTA OUTPUT----------------------------------//
    
    private void actualizarOutput() {
        inventarioDao dao = new inventarioDao();
        lista = dao.listar();
        DefaultListModel datos = new DefaultListModel();
        for (int i = 0; i < lista.size(); i++) {          //for para recorrer los la lista e iterar sobre los items en ella
            Lista elemento = lista.get(i);              // toma todos los datos agregados como elementos
            datos.addElement(elemento.getItemCompleto());     // toma todos los datos y los agrega al elemento
        }
        this.outputList.setModel(datos);             //muestra los elementos guardados en datos en el output
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
